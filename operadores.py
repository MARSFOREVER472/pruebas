# OPERADORES COMPARATIVOS EN PYTHON.

print(str('Vamos a verificar si es que cada una de estas aseveraciones se cumple o no de la siguiente manera:'))

print(str('Ejercicio 1: 4 < 8'))
print(4 < 8) # Si 4 es menor que 8.

print(str('Ejercicio 2: 4 > 8'))
print(4 > 8) # Si 4 es mayor que 8.
print(str('Respuesta: 4 tiene que ser menor que 8'))

print(str('Ejercicio 3: 4 = 8'))
print(4 == 8) # Si 4 es igual que 8.
print(str('Respuesta: 4 tiene que ser igual que 4'))

print(str('Ejercicio 4: 4 <= 8'))
print(4 <= 8) # Si 4 es menor o igual que 8.

print(str('Ejercicio 5: 4 >= 8'))
print(4 >= 8) # Si 4 es mayor o igual que 8.
print(str('Respuesta: 4 tiene que ser menor o igual que 8'))

print(str('Ejercicio 6: 4 != 8'))
print(4 != 8) # Si 4 es distinto que 8.

